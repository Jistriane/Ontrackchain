export type MessageKey = keyof (typeof dictionaries)["pt-BR"];

type InterpolationValues = Record<string, string | number>;

function interpolate(template: string, values?: InterpolationValues): string {
  if (!values) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`));
}

export function normalizeLocale(value: string | null | undefined): Locale {
  if (!value) {
    return DEFAULT_LOCALE;
  }
  const normalized = value.trim().toLowerCase();
  if (normalized === "pt" || normalized === "pt-br") {
    return "pt-BR";
  }
  if (normalized === "en" || normalized === "en-us" || normalized === "en-gb") {
    return "en";
  }
  if (normalized === "es" || normalized === "es-es" || normalized === "es-mx") {
    return "es";
  }
  return DEFAULT_LOCALE;
}

export function getDictionary(locale: Locale) {
  return dictionaries[locale];
}

export function translate(locale: Locale, key: MessageKey, values?: InterpolationValues): string {
  const dictionary = getDictionary(locale);
  const template = dictionary[key] ?? dictionaries[DEFAULT_LOCALE][key] ?? key;
  return interpolate(template, values);
}
